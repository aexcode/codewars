const hello = (name) => {
  return `Hello, ${name}`
}

hello('World') // -> 'Hello, World')
hello('Grae') // -> 'Hello, Grae')
hello('Dan') // -> 'Hello, Dan')
hello('Alex') // -> 'Hello, Alex')
hello('Bethany') // -> 'Hello, Bethany')
hello('Darrell') // -> 'Hello, Darrell')
